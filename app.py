def run_dhruv_engine():
    """Simulates the backend detection process for the demo."""
    time.sleep(2) # Simulating heavy computation
    is_fake = np.random.choice([True, False], p=[0.4, 0.6])
    conf = np.random.uniform(92.4, 99.8)
    
    # Forensic metrics for the Radar Chart
    scores = {
        "Facial Texture": np.random.randint(70, 95) if is_fake else np.random.randint(10, 30),
        "Audio Frequency": np.random.randint(65, 90) if is_fake else np.random.randint(5, 25),
        "Lip-Sync Lag": np.random.randint(60, 99) if is_fake else np.random.randint(0, 15),
        "Blink Consistency": np.random.randint(75, 95) if is_fake else np.random.randint(10, 40),
        "Lighting Noise": np.random.randint(50, 80) if is_fake else np.random.randint(5, 20)
    }
    return is_fake, conf, scores

def create_report_text(name, fake, conf, scores):
    """Generates the downloadable text report."""
    report = f"""
    ==================================================
    FORENSIC AUDIT REPORT: DHRUV'S DEEPFAKE DETECTOR
    ==================================================
    Timestamp: {datetime.now().strftime('%Y-%m-%d %H:%M:%S')}
    File Inspected: {name}
    Verdict: {'MANIPULATED / FAKE' if fake else 'AUTHENTIC / REAL'}
    Confidence Score: {conf:.2f}%
    
    DETAILED ANALYSIS METRICS:
    --------------------------
    """
    for key, val in scores.items():
        report += f"- {key}: {val}/100 Risk Level\n"
    
    report += "\nFinal Summary:\n"
    if fake:
        report += "The AI detected high-frequency noise in the facial region and a mismatch in the audio-visual synchronization typical of GAN-based generation."
    else:
        report += "The media displays natural biometric signals and consistent light gradients. No synthetic artifacts were found."
    
    report += "\n\nGenerated by: DeepScan (Dhruv's Forensic Guard v4.0)"
    return report

