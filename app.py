def show_dashboard():
    st.title("🛡️ Dhruv's Forensic Dashboard")
    st.write("Perform deep-level multimodal analysis on suspicious media.")
    st.divider()

    col_up, col_res = st.columns([1.2, 0.8])

    with col_up:
        st.markdown("<div class='dhruv-card'>", unsafe_allow_html=True)
        st.subheader("📁 Upload Media")
        file = st.file_uploader("Select Video/Audio", type=['mp4', 'mov', 'wav', 'avi'])
        if file:
            st.video(file)
            if st.button("⚡ EXECUTE DHRUV'S AUDIT ENGINE"):
                with st.status("Analyzing Data Streams...", expanded=True):
                    st.write("Extracting facial mesh...")
                    time.sleep(1)
                    st.write("Checking audio-visual sync...")
                    time.sleep(1)
                
                is_fake, conf, scores = run_dhruv_engine()
                st.session_state.analysis_data = (is_fake, conf, scores, file.name)
                st.session_state.audit_logs.append({"Time": datetime.now().strftime("%H:%M"), "File": file.name, "Result": "Fake" if is_fake else "Real"})
        st.markdown("</div>", unsafe_allow_html=True)

    with col_res:
        if st.session_state.analysis_data:
            fake, conf, scores, name = st.session_state.analysis_data
            st.markdown("<div class='dhruv-card'>", unsafe_allow_html=True)
            st.subheader("Audit Verdict")
            
            if fake:
                st.markdown("<span class='status-badge badge-fake'>FAKE DETECTED</span>", unsafe_allow_html=True)
                st.error(f"High risk found with {conf:.1f}% confidence.")
            else:
                st.markdown("<span class='status-badge badge-real'>REAL VERIFIED</span>", unsafe_allow_html=True)
                st.success(f"Authentic media verified at {conf:.1f}% confidence.")
            
            # --- DOWNLOAD REPORT FEATURE ---
            report_content = create_report_text(name, fake, conf, scores)
            st.download_button(
                label="📥 Download Forensic Report",
                data=report_content,
                file_name=f"Dhruv_Report_{name}.txt",
                mime="text/plain"
            )
            st.markdown("</div>", unsafe_allow_html=True)

            # Radar Chart
            fig = go.Figure()
            fig.add_trace(go.Scatterpolar(r=list(scores.values()), theta=list(scores.keys()), fill='toself', line_color='#58a6ff'))
            fig.update_layout(polar=dict(radialaxis=dict(visible=True, range=[0, 100]), bgcolor="#1c2128"), paper_bgcolor="rgba(0,0,0,0)", font_color="white", height=300, showlegend=False)
